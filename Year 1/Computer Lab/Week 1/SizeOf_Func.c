#include <stdio.h>
void main()
{
    int i;
    char c;
    float f;
    double d;
    long int li;
    long double ld;
    printf("Size of int in bytes: %d\n", sizeof(i));
    printf("Size of char in bytes: %d\n", sizeof(c));
    printf("Size of float in bytes: %d\n", sizeof(f));
    printf("Size of double in bytes: %d\n", sizeof(d));
    printf("Size of long int in bytes: %d\n", sizeof(li));
    printf("Size of long double in bytes: %d\n", sizeof(ld));

}
